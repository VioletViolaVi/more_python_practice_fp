def cast_bunny_magic(magic_words):
    if magic_words == "abracadabra":
        print("abracadabra used, bunny be gone")
    elif magic_words == "hey presto":
        print("hey presto used, bunny be gone")
    elif magic_words == "open sesame":
        print("open sesame used, bunny be gone")
    elif magic_words == "bi bi boo":
        print("The bunny explodes")
    else:
        print("magic not working y'all")

